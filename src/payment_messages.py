INVOICE_SEND_ERROR_MESSAGES = {
    "PAYMENT_PROVIDER_INVALID": (
        "Сейчас не получается открыть оплату: платежный провайдер временно недоступен. "
        "Попробуйте позже. Если ошибка повторится, напишите в поддержку."
    ),
    "PAYMENT_PROVIDER_UNSUPPORTED": (
        "Сейчас этот способ оплаты недоступен в Telegram. "
        "Попробуйте позже или напишите в поддержку."
    ),
    "CURRENCY_TOTAL_AMOUNT_INVALID": (
        "Не получилось открыть оплату из-за ошибки в сумме заказа. "
        "Напишите в поддержку, мы быстро проверим тариф."
    ),
    "PRICE_TOTAL_AMOUNT_INVALID": (
        "Не получилось открыть оплату из-за ошибки в сумме заказа. "
        "Напишите в поддержку, мы быстро проверим тариф."
    ),
    "INVOICE_PAYLOAD_INVALID": (
        "Не получилось открыть оплату: счет сформировался некорректно. "
        "Вернитесь к выбору тарифа и попробуйте еще раз."
    ),
    "START_PARAMETER_INVALID": (
        "Не получилось открыть оплату: счет сформировался некорректно. "
        "Вернитесь к выбору тарифа и попробуйте еще раз."
    ),
}


def format_invoice_send_error(exc):
    error_text = str(exc)
    for marker, message in INVOICE_SEND_ERROR_MESSAGES.items():
        if marker in error_text:
            return message

    return (
        "Не удалось открыть форму оплаты. "
        "Попробуйте выбрать тариф еще раз. Если ошибка повторится, напишите в поддержку."
    )
